"""Measure end-to-end latency: no-cache baseline vs full Asteria cache.

This is the **slow** harness. It runs every query in `data/paraphrases.csv`
(60 in total — 20 originals + 40 paraphrases) through the real plan-execute
agent twice:

    Pass 1 — no cache       : every query goes through discovery + planning +
                              execution + summarization. Each query is fresh.
    Pass 2 — full Asteria   : same agent, same model, but with the AsteriaCache
                              wired in. Originals run first (they will MISS
                              and seed the cache), paraphrases run after them
                              (and should HIT).

Per-query timings come from `timer.RunTiming` (the same dataclass timer.py
already prints), so the numbers in this benchmark are directly comparable to
anything else generated by `timer.py`.

Reported metrics:
    - per-query total_s  (no-cache vs with-cache)
    - aggregate average / median latency (over all 60, then originals-only and
      paraphrases-only)
    - latency improvement (s and %) for paraphrases — this is the headline
      number for the report
    - cache hit/miss flags from the cached run

Caveats — read before reporting numbers:
  - This is end-to-end *real* latency; depends on WatsonX response time
    and on the local MCP servers being up. Re-run on stable infrastructure.
  - Some queries may legitimately fail (e.g., asset not present in the local
    CouchDB). Failed runs are recorded with the error and excluded from
    aggregate averages but kept in the per-query detail dump.
  - The cached pass is run AFTER the no-cached pass to avoid one warming the
    other. Each pass uses a fresh ProfiledRunner instance.

Usage (from the repo root, with .env populated and MCP servers running):
    python asteria_temporal_eval/run_latency_benchmark.py
    python asteria_temporal_eval/run_latency_benchmark.py --skip-summary --runs 1

The script does NOT require sentence-transformers/torch unless you actually
flip into the cached pass. Pass `--no-cached-pass` to only measure the
baseline (useful as a first dry-run).
"""

from __future__ import annotations

import argparse
import asyncio
import csv
import json
import statistics
import sys
import time
import traceback
from collections import defaultdict
from pathlib import Path
from typing import Any

ROOT = Path(__file__).resolve().parent
DATA_DIR = ROOT / "data"
REPO_ROOT = ROOT.parent
SRC_DIR = REPO_ROOT / "src"
for p in (REPO_ROOT, SRC_DIR):
    if str(p) not in sys.path:
        sys.path.insert(0, str(p))


def load_queries() -> list[dict[str, Any]]:
    path = DATA_DIR / "paraphrases.csv"
    if not path.exists():
        raise SystemExit(
            f"missing {path}\n"
            f"run `python asteria_temporal_eval/generate_paraphrases.py` first."
        )
    rows: list[dict[str, Any]] = []
    with path.open(encoding="utf-8") as f:
        for r in csv.DictReader(f):
            if r["role"] == "paraphrase" and not r["text"].strip():
                continue
            rows.append({
                "query_id":         int(r["query_id"]),
                "role":             r["role"],
                "parent_query_id":  int(r["parent_query_id"]),
                "mcp_type":         r["mcp_type"],
                "temporal_class":   r["temporal_class"],
                "gen_temperature":  float(r["gen_temperature"]) if r["gen_temperature"] else 0.0,
                "text":             r["text"],
            })

    # Deterministic order: originals first (lowest query_id), then paraphrases.
    rows.sort(key=lambda r: (0 if r["role"] == "original" else 1, r["query_id"]))
    return rows


async def run_pass(label: str, queries: list[dict[str, Any]], args: argparse.Namespace,
                   *, full_asteria: bool) -> list[dict[str, Any]]:
    from timer import ProfiledRunner

    print(f"\n{'═' * 64}\n  PASS: {label}  ({len(queries)} queries)\n{'═' * 64}")
    runner = ProfiledRunner(
        model_id=args.model_id,
        summarize=not args.skip_summary,
        full_asteria=full_asteria,
    )

    out: list[dict[str, Any]] = []
    for i, q in enumerate(queries, 1):
        head = f"[{i:>2}/{len(queries)}] qid={q['query_id']:>2} ({q['role']:<10} {q['temporal_class']:<8})"
        print(f"\n{head}: {q['text'][:90]}", flush=True)
        wall_t0 = time.perf_counter()
        try:
            timing = await runner.run(q["text"])
            err = None
        except Exception as exc:  # noqa: BLE001
            err = f"{type(exc).__name__}: {exc}"
            print(f"  ! ERROR: {err}", flush=True)
            traceback.print_exc()
            timing = None

        wall_s = time.perf_counter() - wall_t0
        rec: dict[str, Any] = {
            **q,
            "pass":              label,
            "wall_s":            round(wall_s, 3),
            "error":             err,
        }
        if timing is not None:
            rec.update({
                "discovery_s":       round(timing.discovery_s, 3),
                "planning_s":        round(timing.planning_s, 3),
                "execution_s":       round(timing.execution_s, 3),
                "summarization_s":   round(timing.summarization_s, 3),
                "total_s":           round(timing.total_s, 3),
                "n_steps":           len(timing.steps),
                "asteria_query_hit": bool(timing.asteria_query_hit),
                "query_cache_hit":   bool(timing.query_cache_hit),
            })
            print(
                f"  total={timing.total_s:.2f}s  "
                f"plan={timing.planning_s:.2f}s  "
                f"exec={timing.execution_s:.2f}s  "
                f"sum={timing.summarization_s:.2f}s  "
                f"asteria_hit={timing.asteria_query_hit}",
                flush=True,
            )
        out.append(rec)

    return out


def aggregate(records: list[dict[str, Any]], *, role: str | None = None,
              temporal_class: str | None = None) -> dict[str, Any]:
    rows = [r for r in records if r["error"] is None and "total_s" in r]
    if role is not None:
        rows = [r for r in rows if r["role"] == role]
    if temporal_class is not None:
        rows = [r for r in rows if r["temporal_class"] == temporal_class]
    if not rows:
        return {"n": 0}
    times = [r["total_s"] for r in rows]
    return {
        "n":      len(rows),
        "mean_s": round(statistics.mean(times), 3),
        "median_s": round(statistics.median(times), 3),
        "min_s":  round(min(times), 3),
        "max_s":  round(max(times), 3),
    }


def report(no_cache: list[dict[str, Any]], cached: list[dict[str, Any]] | None) -> dict[str, Any]:
    """Build the summary block. cached can be None if --no-cached-pass."""

    def section(records: list[dict[str, Any]]) -> dict[str, Any]:
        return {
            "all":          aggregate(records),
            "originals":    aggregate(records, role="original"),
            "paraphrases":  aggregate(records, role="paraphrase"),
            "by_temporal_class": {
                cls: aggregate(records, role="paraphrase", temporal_class=cls)
                for cls in ("static", "anchored", "volatile", "relative")
            },
        }

    out = {"no_cache": section(no_cache)}
    if cached is not None:
        out["cached"] = section(cached)

        def diff(a: dict[str, Any], b: dict[str, Any]) -> dict[str, Any]:
            if not a or not b or a.get("n", 0) == 0 or b.get("n", 0) == 0:
                return {"n_a": a.get("n", 0), "n_b": b.get("n", 0)}
            d = a["mean_s"] - b["mean_s"]
            pct = (100 * d / a["mean_s"]) if a["mean_s"] else 0.0
            return {
                "n":            a["n"],
                "mean_s_no_cache":   a["mean_s"],
                "mean_s_cached":     b["mean_s"],
                "saved_s":           round(d, 3),
                "saved_percent":     round(pct, 1),
                "median_s_no_cache": a["median_s"],
                "median_s_cached":   b["median_s"],
            }

        out["improvement"] = {
            "all":         diff(out["no_cache"]["all"],        out["cached"]["all"]),
            "originals":   diff(out["no_cache"]["originals"],  out["cached"]["originals"]),
            "paraphrases": diff(out["no_cache"]["paraphrases"],out["cached"]["paraphrases"]),
            "by_temporal_class_paraphrases": {
                cls: diff(out["no_cache"]["by_temporal_class"][cls],
                          out["cached"]["by_temporal_class"][cls])
                for cls in ("static", "anchored", "volatile", "relative")
            },
        }

        # Cache hit summary on the paraphrases (the headline)
        para_cached = [r for r in cached if r["role"] == "paraphrase" and r.get("error") is None]
        out["paraphrase_hit_rate"] = {
            "n":     len(para_cached),
            "hits":  sum(1 for r in para_cached if r.get("asteria_query_hit")),
            "rate":  round(
                100 * sum(1 for r in para_cached if r.get("asteria_query_hit")) / max(1, len(para_cached)),
                1,
            ),
            "by_temporal_class": {
                cls: {
                    "n":    sum(1 for r in para_cached if r["temporal_class"] == cls),
                    "hits": sum(1 for r in para_cached if r["temporal_class"] == cls and r.get("asteria_query_hit")),
                }
                for cls in ("static", "anchored", "volatile", "relative")
            },
        }

    return out


def print_summary(report_obj: dict[str, Any]) -> None:
    print("\n" + "═" * 72)
    print("  LATENCY SUMMARY")
    print("═" * 72)

    def rowfmt(label: str, agg: dict[str, Any]) -> str:
        if agg.get("n", 0) == 0:
            return f"  {label:<28} (no successful runs)"
        return (f"  {label:<28} n={agg['n']:>2}  "
                f"mean={agg['mean_s']:>6.2f}s  median={agg['median_s']:>6.2f}s  "
                f"min={agg['min_s']:>5.2f}  max={agg['max_s']:>5.2f}")

    print("\n  ── NO CACHE ─────────────────────────────────────────────────────────────")
    print(rowfmt("all 60 queries",            report_obj["no_cache"]["all"]))
    print(rowfmt("originals (20)",            report_obj["no_cache"]["originals"]))
    print(rowfmt("paraphrases (40)",          report_obj["no_cache"]["paraphrases"]))
    for cls in ("static", "anchored", "volatile", "relative"):
        print(rowfmt(f"  paraphrases [{cls}]", report_obj["no_cache"]["by_temporal_class"][cls]))

    if "cached" not in report_obj:
        return

    print("\n  ── WITH ASTERIA CACHE ──────────────────────────────────────────────────")
    print(rowfmt("all 60 queries",            report_obj["cached"]["all"]))
    print(rowfmt("originals (20)",            report_obj["cached"]["originals"]))
    print(rowfmt("paraphrases (40)",          report_obj["cached"]["paraphrases"]))
    for cls in ("static", "anchored", "volatile", "relative"):
        print(rowfmt(f"  paraphrases [{cls}]", report_obj["cached"]["by_temporal_class"][cls]))

    print("\n  ── IMPROVEMENT (no-cache → cached) ─────────────────────────────────────")
    imp = report_obj["improvement"]
    for label, key in [("all 60 queries", "all"),
                       ("originals", "originals"),
                       ("paraphrases", "paraphrases")]:
        d = imp[key]
        if d.get("n", 0) == 0:
            print(f"  {label:<28} (insufficient data)")
            continue
        print(f"  {label:<28} mean: {d['mean_s_no_cache']:>6.2f}s → "
              f"{d['mean_s_cached']:>6.2f}s   saved={d['saved_s']:>6.2f}s "
              f"({d['saved_percent']:>+5.1f}%)")
    for cls in ("static", "anchored", "volatile", "relative"):
        d = imp["by_temporal_class_paraphrases"][cls]
        if d.get("n", 0) == 0:
            print(f"    paraphrases [{cls}]:           (no successful runs)")
            continue
        print(f"    paraphrases [{cls}]:        mean: {d['mean_s_no_cache']:>6.2f}s → "
              f"{d['mean_s_cached']:>6.2f}s   saved={d['saved_s']:>6.2f}s "
              f"({d['saved_percent']:>+5.1f}%)")

    print("\n  ── PARAPHRASE CACHE HIT RATE (cached pass) ─────────────────────────────")
    h = report_obj["paraphrase_hit_rate"]
    print(f"  overall                      n={h['n']}  hits={h['hits']}  rate={h['rate']}%")
    for cls in ("static", "anchored", "volatile", "relative"):
        c = h["by_temporal_class"][cls]
        if c["n"]:
            rate = 100 * c["hits"] / c["n"]
            print(f"    [{cls:<8}]                 n={c['n']:>2}  hits={c['hits']:>2}  rate={rate:>5.1f}%")


async def amain() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--model-id", default="watsonx/meta-llama/llama-3-3-70b-instruct")
    ap.add_argument("--skip-summary", action="store_true",
                    help="Skip final LLM summarization phase (faster, recommended for benchmarking).")
    ap.add_argument("--no-cached-pass", action="store_true",
                    help="Only measure the no-cache baseline. Useful as a first dry-run.")
    ap.add_argument("--output", default=str(DATA_DIR / "latency_results.json"))
    args = ap.parse_args()

    from dotenv import load_dotenv
    load_dotenv()

    queries = load_queries()
    print(f"loaded {len(queries)} queries from {DATA_DIR / 'paraphrases.csv'}")
    print(f"model: {args.model_id}   skip_summary={args.skip_summary}")

    no_cache_records = await run_pass("no_cache", queries, args, full_asteria=False)

    cached_records: list[dict[str, Any]] | None = None
    if not args.no_cached_pass:
        cached_records = await run_pass("cached_full_asteria", queries, args, full_asteria=True)

    summary = report(no_cache_records, cached_records)
    summary["model_id"] = args.model_id
    summary["skip_summary"] = args.skip_summary
    summary["per_query"] = {
        "no_cache": no_cache_records,
        "cached":   cached_records or [],
    }

    print_summary(summary)

    out = Path(args.output)
    out.write_text(json.dumps(summary, indent=2), encoding="utf-8")
    print(f"\nwrote {out}")


def main() -> None:
    asyncio.run(amain())


if __name__ == "__main__":
    main()
